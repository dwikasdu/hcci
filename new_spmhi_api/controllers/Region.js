var mysqlLib = require('../connection/mysql_connection')

const Provinsi = (req, res) => {
    mysqlLib.executeQuery("SELECT * FROM provinces").then((d) => {
        res.status(200).json({
            status: res.statusCode,
            message: "Sukses",
            data: d
        })
    }).catch(e => {
        console.log(e);
        res.status(505).json({
            status: res.statusCode,
            authentication: false,
            message: e.stack
        })
    });
}

